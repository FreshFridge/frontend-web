export function getInitialCountry() {
  try {
    const region = new Intl.Locale(navigator.language).region;
    return region && countryCodes.includes(region) ? region : "UA";
  } catch {
    return "UA";
  }
}

export function getCountryOptions(displayLocale: string) {
  const displayNames = new Intl.DisplayNames([displayLocale], {
    type: "region",
  });

  return countryCodes
    .map((code) => ({
      code,
      name: displayNames.of(code) ?? code,
    }))
    .sort((a, b) => a.name.localeCompare(b.name, displayLocale));
}

export function getCountrySettings(countryCode: string) {
  const browserLanguage = navigator.language.split("-")[0] || "en";
  const language = languageByCountry[countryCode] ?? browserLanguage;
  const timezone =
    timezoneByCountry[countryCode] ??
    Intl.DateTimeFormat().resolvedOptions().timeZone ??
    "UTC";

  return {
    locale: `${language}-${countryCode}`,
    timezone,
  };
}
